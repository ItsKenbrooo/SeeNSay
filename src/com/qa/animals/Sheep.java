package com.qa.animals;

class Sheep {
		
	private double twos;
	private double fives;
	private double tens;
	
	public Sheep(double twos, double fives, double tens) {
		super();
		this.twos = twos;
		this.fives = fives;
		this.tens = tens;
	}

	public double getTwos() {
		return twos;
	}

	public void setTwos(double twos) {
		this.twos = twos;
	}

	public double getFives() {
		return fives;
	}

	public void setFives(double fives) {
		this.fives = fives;
	}

	public double getTens() {
		return tens;
	}

	public void setTens(double tens) {
		this.tens = tens;
	}
	
	
	
	
}
